    private void jbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarActionPerformed
        lugar = new Lugar();
        lugar.setIdLugares(Integer.parseInt(jlId.getText()));
        try {
            controlador.eliminar(lugar);
            GestionLugares gestionLugares = new GestionLugares();
            escritorio.removeAll();
            gestionLugares = new GestionLugares(); 
            int width = escritorio.getWidth();
            int Height = escritorio.getHeight();
            gestionLugares.setSize(width, Height);
            escritorio.add(gestionLugares);
            gestionLugares.show();
        } catch (SQLException ex) {
            Logger.getLogger(AlertaConfirmar.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
        AlertaCorrecto alertaCorrecto = new AlertaCorrecto(new JFrame(), true);
        alertaCorrecto.jlCorrecto.setText("SE ELIMINÃ“ CORRECTAMENTE");
        alertaCorrecto.setVisible(true);
    }//GEN-LAST:event_jbAceptarActionPerformed

