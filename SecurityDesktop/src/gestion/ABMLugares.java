    @Override
    public int grabar() {
        if (jbNuevo.getText().equals("REGISTRAR")) {
            lugar = new Lugar();
            lugar.setNombreLugar(jtfNombreLugar.getText());
            lugar.setUbicacion(jtfUbicacion.getText());
            lugar.setEmail(jtfEmail.getText());
            if (jrbActivo.isSelected()) {
                lugar.setEstado("1");
            } else {
                lugar.setEstado("0");
            }
            if (ComprobarImagen == false) {
                AlertaError alertaError = new AlertaError(new JFrame(), true);
                alertaError.jlError.setText("DEBE SELECCIONAR UNA IMAGEN");
                alertaError.setVisible(true);
            }
            File ruta = new File(jlRutaImagen.getText());
            byte[] icono = new byte[(int) ruta.length()];
            try {
                InputStream input = new FileInputStream(ruta);
                input.read(icono);
                lugar.setImagen(icono);
                controlador.insetar(lugar);
                AlertaCorrecto alertaCorrecto = new AlertaCorrecto(new JFrame(), true);
                alertaCorrecto.jlCorrecto.setText("SE REGISTRO CORRECTAMENTE");
                alertaCorrecto.setVisible(true);
                inicio();
            } catch (Exception ex) {
                AlertaError alertaError = new AlertaError(new JFrame(), true);
                alertaError.jlError.setText("ERROR AL REGISTRAR");
                alertaError.setVisible(true);
            }
            return IABM.ok;
        } 
        
        else if (jbNuevo.getText().equals("MODIFICAR")) {
            lugar = new Lugar();
            lugar.setNombreLugar(jtfNombreLugar.getText());
            lugar.setUbicacion(jtfUbicacion.getText());
            lugar.setEmail(jtfEmail.getText());
            if (jrbActivo.isSelected()) {
                lugar.setEstado("1");
            } else {
                lugar.setEstado("0");
            }
            lugar.setIdLugares(Integer.parseInt(this.jlId.getText()));
            if (!jlRutaImagen.getText().equals("")) {
                File ruta = new File(jlRutaImagen.getText());

                byte[] icono = new byte[(int) ruta.length()];
                try {
                    InputStream input = new FileInputStream(ruta);
                    input.read(icono);
                    lugar.setImagen(icono);
                    controlador.modificar(lugar);
                    AlertaCorrecto alertaCorrecto = new AlertaCorrecto(new JFrame(), true);
                    alertaCorrecto.jlCorrecto.setText("SE MODIFICÃ“ CORRECTAMENTE");
                    alertaCorrecto.setVisible(true);
                } catch (Exception ex) {
                    AlertaError alertaError = new AlertaError(new JFrame(), true);
                    alertaError.jlError.setText("ERROR AL MODIFICAR");
                    alertaError.setVisible(true);
                }

                return IABM.ok;
            } else {
                try {
                    ComprobarImagen = true;
                    controlador.modificarSinImagen(lugar);
                    AlertaCorrecto alertaCorrecto = new AlertaCorrecto(new JFrame(), true);
                    alertaCorrecto.jlCorrecto.setText("SE MODIFICÃ“ CORRECTAMENTE");
                    alertaCorrecto.setVisible(true);
                } catch (SQLException ex) {
                    AlertaError alertaError = new AlertaError(new JFrame(), true);
                    alertaError.jlError.setText("ERROR AL MODIFICAR");
                    alertaError.setVisible(true);

                }
                return IABM.ok;
            }

        }
        return IABM.nok;
    }

    @Override
    public int comprobarErrores() {
        if (jtfNombreLugar.getText().trim().isEmpty() || jtfUbicacion.getText().trim().isEmpty() || jtfEmail.getText().trim().isEmpty()) {
            return IABM.error;
        }
        if (jrbActivo.isSelected() == false && jrbInactivo.isSelected() == false) {
            return IABM.error;
        }
        return IABM.ok;
    }

    @Override
    public void inicio() {
        jtfNombreLugar.setText("");
        jtfUbicacion.setText("");
        jtfEmail.setText("");
        jlImagen.setIcon(null);
        jlRutaImagen.setText(null);
        ComprobarImagen = false;
    }

